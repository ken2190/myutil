def run_model(input_sequence, output_size):
    """function run_model
    Args:
        input_sequence:   
        output_size:   
    Returns:
        
    """
    access_config = {
        "memory_size": memory_size,
        "word_size": word_size,
        "num_reads": num_read_heads,
        "num_writes": num_write_heads,
    }
    controller_config = {"hidden_size": hidden_size}

    dnc_core = dnc.DNC(access_config, controller_config, output_size, clip_value)
    initial_state = dnc_core.initial_state(batch_size)
    output_sequence, _ = tf.nn.dynamic_rnn(
        cell=dnc_core, inputs=input_sequence, time_major=False, initial_state=initial_state
    )
    return output_sequence


# In[14]:


class Model:
    def __init__(self, dict_size):
        """ Model:__init__
        Args:
            dict_size:     
        Returns:
           
        """

        self.X = tf.placeholder(tf.int32, [None, None])
        self.Y = tf.placeholder(tf.float32, [None, dimension_output])
        encoder_embeddings = tf.Variable(tf.random_uniform([dict_size, embedded_size], -1, 1))
        encoder_embedded = tf.nn.embedding_lookup(encoder_embeddings, self.X)
        outputs = run_model(encoder_embedded, size_layer)
        W = tf.get_variable(
            "w", shape=(size_layer, dimension_output), initializer=tf.orthogonal_initializer()
        )
        b = tf.get_variable("b", shape=(dimension_output), initializer=tf.zeros_initializer())
        self.logits = tf.matmul(outputs[:, -1], W) + b
        self.cost = tf.reduce_mean(
            tf.nn.softmax_cross_entropy_with_logits(logits=self.logits, labels=self.Y)
        )
        self.optimizer = tf.train.AdamOptimizer(learning_rate=learning_rate).minimize(self.cost)
        correct_pred = tf.equal(tf.argmax(self.logits, 1), tf.argmax(self.Y, 1))
        self.accuracy = tf.reduce_mean(tf.cast(correct_pred, tf.float32))


# In[15]:


tf.reset_default_graph()
